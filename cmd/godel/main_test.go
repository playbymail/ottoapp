func TestEndToEnd(t *testing.T) {
	// Create a temporary test file with a realistic struct
	testFile := filepath.Join(t.TempDir(), "handlers.go")
	testCode := `package handlers

import "time"

type UserView struct {
	ID          string          ` + "`jsonapi:\"primary,users\"`" + `
	Username    string          ` + "`jsonapi:\"attr,username\"`" + `
	Email       string          ` + "`jsonapi:\"attr,email\"`" + `
	Timezone    string          ` + "`jsonapi:\"attr,timezone\"`" + `
	Roles       []string        ` + "`jsonapi:\"attr,roles,omitempty\"`" + `
	Permissions map[string]bool ` + "`jsonapi:\"attr,permissions,omitempty\"`" + `
	CreatedAt   time.Time       ` + "`jsonapi:\"attr,createdAt,iso8601\"`" + `
	UpdatedAt   time.Time       ` + "`jsonapi:\"attr,updatedAt,iso8601\"`" + `
}
`

	if err := os.WriteFile(testFile, []byte(testCode), 0644); err != nil {
		t.Fatalf("failed to create test file: %v", err)
	}

	// Parse the struct
	model, err := parseStruct(testFile, "UserView")
	if err != nil {
		t.Fatalf("parseStruct failed: %v", err)
	}

	// Generate the model
	outputFile := filepath.Join(t.TempDir(), "user.js")
	if err := generateEmberModel(model, outputFile); err != nil {
		t.Fatalf("generateEmberModel failed: %v", err)
	}

	// Verify the generated file
	content, err := os.ReadFile(outputFile)
	if err != nil {
		t.Fatalf("failed to read generated file: %v", err)
	}

	generated := string(content)

	// Check for all expected fields
	expectedFields := []string{
		"@attr('string') username;",
		"@attr('string') email;",
		"@attr('string') timezone;",
		"@attr() roles;",
		"@attr() permissions;",
		"@attr('date') createdAt;",
		"@attr('date') updatedAt;",
	}

	for _, expected := range expectedFields {
		if !strings.Contains(generated, expected) {
			t.Errorf("generated file missing expected field: %s", expected)
		}
	}

	// Verify model class name
	if !strings.Contains(generated, "export default class UsersModel extends Model") {
		t.Error("generated file missing correct class declaration")
	}
}
