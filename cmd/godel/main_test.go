func TestParseStruct(t *testing.T) {
	// Create a temporary test file
	testFile := filepath.Join(t.TempDir(), "test.go")
	testCode := `package test

import "time"

type TestView struct {
	ID          string          ` + "`jsonapi:\"primary,tests\"`" + `
	Name        string          ` + "`jsonapi:\"attr,name\"`" + `
	Email       string          ` + "`jsonapi:\"attr,email\"`" + `
	Age         int             ` + "`jsonapi:\"attr,age\"`" + `
	IsActive    bool            ` + "`jsonapi:\"attr,isActive\"`" + `
	CreatedAt   time.Time       ` + "`jsonapi:\"attr,createdAt,iso8601\"`" + `
	Tags        []string        ` + "`jsonapi:\"attr,tags,omitempty\"`" + `
	Settings    map[string]bool ` + "`jsonapi:\"attr,settings,omitempty\"`" + `
}
`

	if err := os.WriteFile(testFile, []byte(testCode), 0644); err != nil {
		t.Fatalf("failed to create test file: %v", err)
	}

	model, err := parseStruct(testFile, "TestView")
	if err != nil {
		t.Fatalf("parseStruct failed: %v", err)
	}

	// Verify model name (uses exact name from jsonapi tag, not singularized)
	if model.Name != "tests" {
		t.Errorf("expected model name 'tests', got '%s'", model.Name)
	}

	// Verify field count
	expectedFields := 7
	if len(model.Fields) != expectedFields {
		t.Errorf("expected %d fields, got %d", expectedFields, len(model.Fields))
	}

	// Verify specific fields
	tests := []struct {
		name      string
		emberType string
		isArray   bool
		isObject  bool
	}{
		{"name", "string", false, false},
		{"email", "string", false, false},
		{"age", "number", false, false},
		{"isActive", "boolean", false, false},
		{"createdAt", "date", false, false},
		{"tags", "", true, false},
		{"settings", "", false, true},
	}

	for _, tt := range tests {
		found := false
		for _, field := range model.Fields {
			if field.Name == tt.name {
				found = true
				if field.EmberType != tt.emberType {
					t.Errorf("field %s: expected type '%s', got '%s'", tt.name, tt.emberType, field.EmberType)
				}
				if field.IsArray != tt.isArray {
					t.Errorf("field %s: expected isArray %v, got %v", tt.name, tt.isArray, field.IsArray)
				}
				if field.IsObject != tt.isObject {
					t.Errorf("field %s: expected isObject %v, got %v", tt.name, tt.isObject, field.IsObject)
				}
				break
			}
		}
		if !found {
			t.Errorf("field %s not found in parsed model", tt.name)
		}
	}
}

