func TestMapGoTypeToEmber(t *testing.T) {
	tests := []struct {
		goType    string
		emberType string
		isArray   bool
		isObject  bool
	}{
		{"string", "string", false, false},
		{"time.Time", "date", false, false},
		{"bool", "boolean", false, false},
		{"int", "number", false, false},
		{"int64", "number", false, false},
		{"float64", "number", false, false},
		{"[]string", "", true, false},
		{"[]int", "", true, false},
		{"map[string]bool", "", false, true},
		{"map[string]string", "", false, true},
	}

	for _, tt := range tests {
		emberType, isArray, isObject := mapGoTypeToEmber(tt.goType)
		if emberType != tt.emberType {
			t.Errorf("mapGoTypeToEmber(%s): expected type '%s', got '%s'", tt.goType, tt.emberType, emberType)
		}
		if isArray != tt.isArray {
			t.Errorf("mapGoTypeToEmber(%s): expected isArray %v, got %v", tt.goType, tt.isArray, isArray)
		}
		if isObject != tt.isObject {
			t.Errorf("mapGoTypeToEmber(%s): expected isObject %v, got %v", tt.goType, tt.isObject, isObject)
		}
	}
}

func TestGenerateEmberModel(t *testing.T) {
	model := &Model{
		Name: "users",
		Fields: []Field{
			{Name: "username", EmberType: "string"},
			{Name: "email", EmberType: "string"},
			{Name: "age", EmberType: "number"},
			{Name: "createdAt", EmberType: "date"},
			{Name: "tags", IsArray: true},
			{Name: "settings", IsObject: true},
		},
	}

	outputFile := filepath.Join(t.TempDir(), "user.js")
	if err := generateEmberModel(model, outputFile); err != nil {
		t.Fatalf("generateEmberModel failed: %v", err)
	}

	// Read generated file
	content, err := os.ReadFile(outputFile)
	if err != nil {
		t.Fatalf("failed to read generated file: %v", err)
	}

	generated := string(content)

	// Verify content
	expectedStrings := []string{
		"// Generated by godel - DO NOT EDIT",
		"import Model, { attr } from '@ember-data/model'",
		"export default class UsersModel extends Model",
		"@attr('string') username;",
		"@attr('string') email;",
		"@attr('number') age;",
		"@attr('date') createdAt;",
		"@attr() tags;",
		"@attr() settings;",
	}

	for _, expected := range expectedStrings {
		if !strings.Contains(generated, expected) {
			t.Errorf("generated file missing expected string: %s", expected)
		}
	}
}

